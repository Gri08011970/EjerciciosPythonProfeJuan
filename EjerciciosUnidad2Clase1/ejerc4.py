base = int(input("Ingrese la longitud de la base en cm: "))
altura = int(input("Ingrese la longitud de la altura en cm: "))

perímetro = (base * 2) + (altura * 2)
superficie = base * altura
print(f"El perímetro del rectángulo es: {perímetro} cm.")

print(f"La superficie del rectángulo es: {superficie} cm2.")
